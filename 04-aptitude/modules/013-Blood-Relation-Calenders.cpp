/**
 *  Predict Day Of Week?
 *  -   1st Nov, 2003was Sunday. What day of the week was it on 1stDec. 2004?
 *      a)  Thursday    b)  Sunday  c) Tuesday  d) Friday
 *  Ans:    Thursday
 *      -   2004 is a leap year hence if 1 November 2003 was Sunday then 1 
 *          November 2004 was Sunday+ 2 more days= Tuesday. There are 30 days 
 *          between 1 November and 1 December. 30 days = 4×7+2.     
 * 
 *          Hence day on 1 December 2004 is Tuesday+ 2= Thursday.
 */

